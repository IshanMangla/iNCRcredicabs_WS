package ncab.beans;

public class SosContactsBean {
	private int sosContactId;
	private String sosNbr;
	private String sosName;
	/**
	 * @return the sosContactId
	 */
	public int getSosContactId() {
		return sosContactId;
	}
	/**
	 * @param sosContactId the sosContactId to set
	 */
	public void setSosContactId(int sosContactId) {
		this.sosContactId = sosContactId;
	}
	/**
	 * @return the sosNbr
	 */
	public String getSosNbr() {
		return sosNbr;
	}
	/**
	 * @param sosNbr the sosNbr to set
	 */
	public void setSosNbr(String sosNbr) {
		this.sosNbr = sosNbr;
	}
	/**
	 * @return the sosName
	 */
	public String getSosName() {
		return sosName;
	}
	/**
	 * @param sosName the sosName to set
	 */
	public void setSosName(String sosName) {
		this.sosName = sosName;
	}
	

}
